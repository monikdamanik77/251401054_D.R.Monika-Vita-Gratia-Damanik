#include <iostream>
#include <cstdlib>
using namespace std;
 //perulangan for
int main () {
    system ("cls");
    int x;

    cout << "inputkan sebuah angka: ";
    cin>>x;

    //inisiasi, kondisi, iterasi
    for (int a=x; a<=10; a++ ) 
        cout << a << endl; 

    // proses 1: a => 1 lalu dicetak kondisi benar => tampilkan dulu baru ditambah
}  